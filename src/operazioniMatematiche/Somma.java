package operazioniMatematiche;


public class Somma {
    public float n1;
    public float n2;
    
    public Somma (float numero1, float numero2) {
        n1 = numero1;
        n2 = numero2;           
    }
    
    public float esegui () {
        
        float Somma;
         Somma = n1 + n2;
         return Somma;
    }
    
    
    
    
}
