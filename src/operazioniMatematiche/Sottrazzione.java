package operazioniMatematiche;


public class Sottrazzione {
    public float n1;
    public float n2;

    public Sottrazzione (float numero1, float numero2) {
        n1 = numero1;
        n2 = numero2;           
    }
    
    public float esegui () {
        
        float Sottrazzione;
         Sottrazzione = n1 - n2;
         return Sottrazzione;
    }
    
    
    
    
}
